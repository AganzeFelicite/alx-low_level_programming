/**
 * add - adds two integers
 *
 * @a: first integer
 * @b: second integer
 * Return: addition of two integers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two integers
 *
 * @a: first integer
 * @b: second integer
 * Return: subtraction of two integers
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two integers
 *
 * @a: first integer
 * @b: second integer
 * Return: multiplication of two integers
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two integers
 *
 * @a: first integer
 * @b: second integer
 * Return: division of two integers
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus of two integers
 *
 * @a: first integer
 * @b: second integer
 * Return: addition of two integers
 */
int mod(int a, int b)
{
	return (a % b);
}
